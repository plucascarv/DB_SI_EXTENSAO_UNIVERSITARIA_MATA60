-- Criar tabelas SE não existirem
CREATE TABLE IF NOT EXISTS TB_PARTICIPANTE(
  ID_PARTICIPANTE INTEGER PRIMARY KEY,
  NM_PRIMEIRO TEXT NOT NULL,
  NM_MEIO TEXT,
  NM_ULTIMO TEXT NOT NULL,
  CD_CPF_PARTICIPANTE TEXT UNIQUE,
  DT_NASCIMENTO TEXT,
  DS_ENDERECO TEXT,
  TP_GENERO TEXT,
  TP_PARTICIPACAO TEXT,
  CD_MATRICULA TEXT UNIQUE,
  NM_INSTITUICAO TEXT
);

CREATE TABLE IF NOT EXISTS TB_ATIVIDADE (
  ID_ATIVIDADE INTEGER PRIMARY KEY,
  DT_ATIVIDADE TEXT NOT NULL,
  HR_ATIVIDADE TEXT NOT NULL,
  DS_LOCAL TEXT NOT NULL,
  TP_ATIVIDADE TEXT NOT NULL,
  NM_AREA_ESTUDO TEXT NOT NULL,
  NM_ATIVIDADE TEXT NOT NULL,
  CARGA_HORARIA INTEGER NOT NULL,
  DS_ATIVIDADE TEXT NOT NULL,
  DS_RELATORIO TEXT
);

CREATE TABLE IF NOT EXISTS RL_PARTICIPA (
  ID_PARTICIPANTE INTEGER NOT NULL,
  ID_ATIVIDADE INTEGER NOT NULL,
  IS_CERTIFICADO TEXT NOT NULL,
  DS_FEEDBACK TEXT,
  PRIMARY KEY (ID_PARTICIPANTE, ID_ATIVIDADE),
  FOREIGN KEY (ID_PARTICIPANTE) REFERENCES TB_PARTICIPANTE(ID_PARTICIPANTE),
  FOREIGN KEY (ID_ATIVIDADE) REFERENCES TB_ATIVIDADE(ID_ATIVIDADE)
);

CREATE TABLE IF NOT EXISTS TB_PARCEIRO (
  ID_PARCEIRO INTEGER PRIMARY KEY,
  NM_PRIMEIRO TEXT NOT NULL,
  NM_MEIO TEXT,
  NM_ULTIMO TEXT NOT NULL,
  CD_CPF_REPRESENTANTE TEXT UNIQUE,
  NM_EMPRESA TEXT NOT NULL,
  TP_CATEGORIA TEXT NOT NULL,
  ID_ATIVIDADE INTEGER NOT NULL,
  FOREIGN KEY (ID_ATIVIDADE) REFERENCES TB_ATIVIDADE(ID_ATIVIDADE)
);

